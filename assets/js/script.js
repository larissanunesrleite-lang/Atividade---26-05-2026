function formatarPreco(valor) {
  return valor.toLocaleString("pt-BR", {
    style: "currency",
    currency: "BRL"
  });
}

function obterTamanhos(produto) {
  if (produto.tamanho === "P, M, G") {
    return ["P", "M", "G"];
  }

  return [];
}

function montarOpcoesTamanho(produto) {
  var tamanhos = obterTamanhos(produto);

  if (tamanhos.length === 0) {
    return `<p><strong>Tamanho:</strong> ${produto.tamanho}</p>`;
  }

  var opcoes = "";

  for (var i = 0; i < tamanhos.length; i++) {
    var tamanho = tamanhos[i];

    opcoes += `
      <label class="opcao-tamanho">
        <input type="radio" name="tamanho-${produto.id}" value="${tamanho}">
        <span>${tamanho}</span>
      </label>
    `;
  }

  return `
    <div class="seletor-tamanho">
      <p><strong>Escolha o tamanho:</strong></p>
      <div class="opcoes-tamanho">
        ${opcoes}
      </div>
    </div>
  `;
}

function tamanhoEscolhido(id) {
  var opcao = document.querySelector(`input[name="tamanho-${id}"]:checked`);

  if (opcao) {
    return opcao.value;
  }

  return "";
}

function limparTexto(texto) {
  return texto
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/s\b/g, "");
}

function mostrarProdutos() {
  var lista = document.getElementById("listaProdutos");
  var pesquisa = limparTexto(document.getElementById("pesquisa").value);
  var categoriaAtual = "";

  lista.innerHTML = "";

  for (var i = 0; i < produtos.length; i++) {
    var produto = produtos[i];
    var textoProduto = (
      produto.nome + " " +
      produto.categoria + " " +
      produto.descricao + " " +
      produto.cor + " " +
      produto.tamanho
    );

    textoProduto = limparTexto(textoProduto);

    if (categoriaSelecionada !== "todos" && produto.categoria !== categoriaSelecionada) {
      continue;
    }

    if (!textoProduto.includes(pesquisa)) {
      continue;
    }

    if (categoriaSelecionada === "todos" && produto.categoria !== categoriaAtual) {
      categoriaAtual = produto.categoria;

      lista.innerHTML += `
        <div class="col-12">
          <h2 class="titulo-categoria">${categoriaAtual}</h2>
        </div>
      `;
    }

    var disponivel = estoqueDisponivel(produto);
    var textoBotao = "Adicionar ao Carrinho";
    var botaoDesabilitado = "";

    if (disponivel <= 0) {
      textoBotao = "Sem estoque";
      botaoDesabilitado = "disabled";
    }

    lista.innerHTML += `
      <div class="col-sm-6 col-lg-3">
        <div class="card">
          <img src="${produto.imagem}" class="card-img-top" alt="${produto.nome}">

          <div class="card-body">
            <h5 class="card-title">${produto.nome}</h5>
            <p class="card-text">${produto.descricao}</p>
            <p><strong>Categoria:</strong> ${produto.categoria}</p>
            ${montarOpcoesTamanho(produto)}
            <p><strong>Cor:</strong> ${produto.cor}</p>
            <p><strong>Quantidade disponivel:</strong> ${disponivel}</p>
            <p class="preco">${formatarPreco(produto.preco)}</p>

            <button class="btn btn-warning btn-sm" onclick="adicionarCarrinho(${produto.id})" ${botaoDesabilitado}>
              ${textoBotao}
            </button>
          </div>
        </div>
      </div>
    `;
  }
}

