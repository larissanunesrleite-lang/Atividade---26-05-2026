function procurarProduto(id) {
  for (var i = 0; i < produtos.length; i++) {
    if (produtos[i].id === id) {
      return produtos[i];
    }
  }
}

function quantidadeProdutoNoCarrinho(id) {
  var quantidade = 0;

  for (var i = 0; i < carrinho.length; i++) {
    if (carrinho[i].id === id) {
      quantidade = quantidade + carrinho[i].quantidade;
    }
  }

  return quantidade;
}

function quantidadeOutrosItensCarrinho(id, tamanho) {
  var quantidade = 0;

  for (var i = 0; i < carrinho.length; i++) {
    if (carrinho[i].id === id && (carrinho[i].tamanho || "Unico") !== tamanho) {
      quantidade = quantidade + carrinho[i].quantidade;
    }
  }

  return quantidade;
}

function estoqueDisponivel(produto) {
  return produto.estoque - quantidadeProdutoNoCarrinho(produto.id);
}

function normalizarCarrinho() {
  var atualizou = false;

  for (var i = 0; i < carrinho.length; i++) {
    var item = carrinho[i];
    var produto = procurarProduto(item.id);

    if (!produto) {
      continue;
    }

    if (!item.imagem) {
      item.imagem = produto.imagem;
      atualizou = true;
    }

    if (!item.tamanho) {
      item.tamanho = produto.tamanho || "Unico";
      atualizou = true;
    }

    if (item.quantidade > produto.estoque) {
      item.quantidade = produto.estoque;
      atualizou = true;
    }
  }

  if (atualizou) {
    salvarCarrinho();
  }
}

function adicionarCarrinho(id) {
  var produto = procurarProduto(id);
  var tamanhos = obterTamanhos(produto);
  var tamanho = produto.tamanho;
  var disponivel = estoqueDisponivel(produto);

  if (disponivel <= 0) {
    alert("Esse produto ja atingiu a quantidade maxima disponivel no estoque.");
    return;
  }

  if (tamanhos.length > 0) {
    tamanho = tamanhoEscolhido(id);

    if (tamanho === "") {
      alert("Escolha o tamanho P, M ou G antes de adicionar ao carrinho.");
      return;
    }
  }

  var item = procurarItemCarrinho(id, tamanho);

  if (item) {
    item.quantidade++;
  } else {
    carrinho.push({
      id: produto.id,
      nome: produto.nome,
      preco: produto.preco,
      imagem: produto.imagem,
      tamanho: tamanho,
      quantidade: 1
    });
  }

  salvarCarrinho();
  mostrarCarrinho();
  mostrarProdutos();
  mostrarNotificacaoCarrinho(produto, tamanho);
}

function quantidadeItensCarrinho() {
  var quantidade = 0;

  for (var i = 0; i < carrinho.length; i++) {
    quantidade = quantidade + carrinho[i].quantidade;
  }

  return quantidade;
}

function mostrarNotificacaoCarrinho(produto, tamanho) {
  var notificacao = document.getElementById("notificacaoCarrinho");

  if (!notificacao) {
    notificacao = document.createElement("div");
    notificacao.id = "notificacaoCarrinho";
    notificacao.className = "notificacao-carrinho";
    document.body.appendChild(notificacao);
  }

  notificacao.innerHTML = `
    <div class="notificacao-cabecalho">
      <span class="icone-sucesso">✓</span>
      <strong>Adicionado ao carrinho</strong>
      <button type="button" onclick="fecharNotificacaoCarrinho()">×</button>
    </div>

    <div class="notificacao-produto">
      <img src="${produto.imagem}" alt="${produto.nome}">
      <div>
        <strong>${produto.nome}</strong>
        <span>Tamanho: ${tamanho || "Unico"}</span>
        <b>${formatarPreco(produto.preco)}</b>
      </div>
    </div>

    <button class="btn-ver-carrinho" type="button" onclick="abrirCarrinhoNotificacao()">
      Ver carrinho (${quantidadeItensCarrinho()})
    </button>
  `;

  notificacao.classList.add("ativa");
}

function fecharNotificacaoCarrinho() {
  var notificacao = document.getElementById("notificacaoCarrinho");

  if (notificacao) {
    notificacao.classList.remove("ativa");
  }
}

function abrirCarrinhoNotificacao() {
  var carrinhoLateral = document.getElementById("carrinhoLateral");

  if (typeof bootstrap !== "undefined" && carrinhoLateral) {
    bootstrap.Offcanvas.getOrCreateInstance(carrinhoLateral).show();
  }

  fecharNotificacaoCarrinho();
}

function procurarItemCarrinho(id, tamanho) {
  for (var i = 0; i < carrinho.length; i++) {
    if (carrinho[i].id === id && (carrinho[i].tamanho || "Unico") === tamanho) {
      return carrinho[i];
    }
  }
}

function mostrarCarrinho() {
  var lista = document.getElementById("listaCarrinho");
  var total = 0;

  lista.innerHTML = "";

  if (carrinho.length === 0) {
    lista.innerHTML = `
      <div class="carrinho-vazio">
        <strong>Seu carrinho esta vazio.</strong>
        <span>Escolha seus produtos favoritos para torcer com estilo.</span>
      </div>
    `;
  }

  for (var i = 0; i < carrinho.length; i++) {
    var item = carrinho[i];
    var produto = procurarProduto(item.id) || {};
    var subtotal = item.preco * item.quantidade;
    total = total + subtotal;
    var imagem = item.imagem || produto.imagem || "";
    var tamanho = item.tamanho || produto.tamanho || "Unico";
    var maximoItem = produto.estoque - quantidadeOutrosItensCarrinho(item.id, tamanho);

    lista.innerHTML += `
      <div class="item-carrinho">
        <img src="${imagem}" alt="${item.nome}">

        <div class="item-carrinho-info">
          <strong>${item.nome}</strong>
          <span>Tamanho: ${tamanho}</span>
          <span>${formatarPreco(item.preco)} cada</span>
        </div>

        <div class="item-carrinho-acoes">
          <div class="controle-quantidade">
            <button type="button" onclick="alterarQuantidade(${item.id}, '${tamanho}', ${item.quantidade - 1})">-</button>
            <input type="number" min="1" max="${maximoItem}" value="${item.quantidade}" onchange="alterarQuantidade(${item.id}, '${tamanho}', this.value)">
            <button type="button" onclick="alterarQuantidade(${item.id}, '${tamanho}', ${item.quantidade + 1})">+</button>
          </div>
          <strong>${formatarPreco(subtotal)}</strong>
          <button class="btn-remover" onclick="removerItem(${item.id}, '${tamanho}')">Remover</button>
        </div>
      </div>
    `;
  }

  document.getElementById("total").innerHTML = formatarPreco(total);
  document.getElementById("subtotalCarrinho").innerHTML = formatarPreco(total);
}

function alterarQuantidade(id, tamanho, quantidade) {
  var item = procurarItemCarrinho(id, tamanho);
  var produto = procurarProduto(id);
  var maximo = produto.estoque - quantidadeOutrosItensCarrinho(id, tamanho);

  item.quantidade = Number(quantidade);

  if (item.quantidade < 1) {
    item.quantidade = 1;
  }

  if (item.quantidade > maximo) {
    item.quantidade = maximo;
    alert("Quantidade maxima disponivel em estoque: " + produto.estoque);
  }

  salvarCarrinho();
  mostrarCarrinho();
  mostrarProdutos();
}

function removerItem(id, tamanho) {
  var novoCarrinho = [];

  for (var i = 0; i < carrinho.length; i++) {
    if (carrinho[i].id !== id || (carrinho[i].tamanho || "Unico") !== tamanho) {
      novoCarrinho.push(carrinho[i]);
    }
  }

  carrinho = novoCarrinho;
  salvarCarrinho();
  mostrarCarrinho();
  mostrarProdutos();
}

function limparCarrinho() {
  carrinho = [];
  salvarCarrinho();
  mostrarCarrinho();
  mostrarProdutos();
}

function salvarCarrinho() {
  localStorage.setItem("carrinho", JSON.stringify(carrinho));
}

function totalCompra() {
  var total = 0;

  for (var i = 0; i < carrinho.length; i++) {
    total = total + carrinho[i].preco * carrinho[i].quantidade;
  }

  return total;
}

